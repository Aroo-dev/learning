package OOP2.Composition.Room;

public class Dimensionsx {
    private int height;
    private int width;

    public Dimensionsx(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
